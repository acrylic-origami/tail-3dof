uint8_t sin2_t(int16_t x, int16_t y, int16_t t) {
	// normalized xy to period of 1-256 (i.e. -1 -> 1 = one oscillation), 1-256 `t` period
	uint16_t r = isqrt((int32_t)x * x + (int32_t)y * y);
	return icos(r + t) + 128;
}
